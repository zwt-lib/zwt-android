package it.unipr.netsec.zwt;


/** Anchor values.
 */
public class ZwtAnchor {
	
	// No constructor.
	private ZwtAnchor() {}

	/** Alignment at TOP */
	public static final int TOP=0x00;
	
	/** Alignment at BOTTOM */
	public static final int BOTTOM=0x10;
	
	/** Alignment at VCENTER */
	public static final int VCENTER=0x01;
	
	/** Alignment at LEFT */
	public static final int LEFT=0x0000;
	
	/** Alignment at RIGHT */
	public static final int RIGHT=0x1000;
	
	/** Alignment at HCENTER */
	public static final int HCENTER=0x0100;
	
	/** BASELINE */
	public static final int BASELINE=0;
	
	/** DOTTED */
	public static final int DOTTED=0x10000;
	
	/** SOLID */
	public static final int SOLID=0x00000;

}
